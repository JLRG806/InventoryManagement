package com.progra.inventory_classes.product;

public abstract class Assets {
	
	public abstract Integer getId_product();
	public abstract void setId_product(Integer id_product);
	public abstract String getProduct_name();
	public abstract void setProduct_name(String product_name);
	public abstract double getPrice();
	public abstract void setPrice(double price);
	public abstract int getStock_quantity();
	public abstract void setStock_quantity(int stock_quantity);


}
